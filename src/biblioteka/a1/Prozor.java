    private void populate() {
        try {
            c = DriverManager.getConnection(BibliotekaA1.URL_BAZE);
            Statement s = c.createStatement();
            ResultSet rs = s.executeQuery("SELECT * FROM Citalac");
            DefaultTableModel dtm = new DefaultTableModel();
            DefaultComboBoxModel<CitalacDO> dcbm = new DefaultComboBoxModel<>();
            dtm.addColumn("Clanska karta");
            dtm.addColumn("JMBG");
            dtm.addColumn("Ime");
            dtm.addColumn("Prezime");
            dtm.addColumn("Adresa");
            while(rs.next()){
                CitalacDO citalac = new CitalacDO();                 
                Object[] red = new Object[5];
                red[0] = citalac.id = rs.getInt("CitalacID");
                red[1] = citalac.jmbg = rs.getString("MaticniBroj");
                red[2] = citalac.ime = rs.getString("Ime");
                red[3] = citalac.prezime = rs.getString("Prezime");
                red[4] = citalac.adresa = rs.getString("Adresa");
                dtm.addRow(red);
                dcbm.addElement(citalac);
            }
            jTable1.setModel(dtm);
            jComboBox1.setModel(dcbm);
        } catch (SQLException ex) {
            Logger.getLogger(Prozor.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this, ex, "Greska", JOptionPane.ERROR_MESSAGE);
        }
    }
